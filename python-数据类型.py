# 使用type（）查看数据类型
print(type(666))
print(type(1.3))
print(type("nh"))


# 使用变量来存储数据类型
String_type = type("he")
Int_type = type(666)
float_type = type(1.3)

print(String_type, Int_type, float_type)


# 数据格式转换

# 将整数转换成字符串
str_type = str(666)
print(type(str_type))

# 将浮点数转换成字符串
str_type1 = str(1.3331)
print(type(str_type1), str_type)
